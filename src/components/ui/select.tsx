const SelectItem = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Item>,
  React.ComponentPropsWithoutRef<typeof SelectPrimitive.Item>
>(({ className, children, ...props }, ref) => {
  const { primaryColor } = useThemeContext();
  const [element, setElement] = React.useState<HTMLElement | null>(null);

  // Function to apply selected styling
  const applySelectedStyling = React.useCallback(
    (element: HTMLElement) => {
      const isSelected = element.getAttribute("data-state") === "checked";
      if (isSelected) {
        element.style.backgroundColor = `${primaryColor}15`;
        element.style.color = primaryColor;
      } else {
        element.style.backgroundColor = "transparent";
        element.style.color = "black";
      }
    },
    [primaryColor]
  );

  // Apply selected styling when element is available
  React.useEffect(() => {
    if (element) {
      // Apply styling immediately
      applySelectedStyling(element);

      // Set up mutation observer to watch for data-state changes
      const observer = new MutationObserver((mutations) => {
        mutations.forEach((mutation) => {
          if (
            mutation.type === "attributes" &&
            mutation.attributeName === "data-state"
          ) {
            applySelectedStyling(element);
          }
        });
      });

      observer.observe(element, {
        attributes: true,
        attributeFilter: ["data-state"],
      });

      return () => observer.disconnect();
    }
  }, [applySelectedStyling, element]);

  // Callback ref to capture the element
  const setRefs = React.useCallback(
    (node: HTMLElement | null) => {
      setElement(node);
      if (typeof ref === "function") {
        ref(node as HTMLDivElement);
      } else if (ref) {
        ref.current = node as HTMLDivElement;
      }
    },
    [ref]
  );

  return (
    <SelectPrimitive.Item
      ref={setRefs}
      className={cn(
        "relative flex w-full cursor-default select-none items-center rounded-sm py-1.5 pl-2 pr-8 text-sm outline-none data-[disabled]:pointer-events-none data-[disabled]:opacity-50 text-black transition-colors duration-200",
        className
      )}
      style={
        {
          "--hover-bg-color": `${primaryColor}15`, // 15% opacity for light hover
          "--hover-text-color": primaryColor,
        } as React.CSSProperties
      }
      onMouseEnter={(e) => {
        e.currentTarget.style.backgroundColor = `${primaryColor}15`;
        e.currentTarget.style.color = primaryColor;
      }}
      onMouseLeave={(e) => {
        // Check if the item is selected
        const isSelected =
          e.currentTarget.getAttribute("data-state") === "checked";
        if (isSelected) {
          // Keep selected styling
          e.currentTarget.style.backgroundColor = `${primaryColor}15`;
          e.currentTarget.style.color = primaryColor;
        } else {
          // Reset to default styling
          e.currentTarget.style.backgroundColor = "transparent";
          e.currentTarget.style.color = "black";
        }
        // Reset the check icon color
        const checkIcon = e.currentTarget.querySelector(
          "[data-radix-select-item-indicator]"
        ) as HTMLElement;
        if (checkIcon) {
          checkIcon.style.color = "currentColor";
        }
      }}
      {...props}
    >
      <span className="absolute right-2 flex h-3.5 w-3.5 items-center justify-center">
        <SelectPrimitive.ItemIndicator>
          <Check className="h-4 w-4" />
        </SelectPrimitive.ItemIndicator>
      </span>
      <SelectPrimitive.ItemText className="text-black">
        {children}
      </SelectPrimitive.ItemText>
    </SelectPrimitive.Item>
  );
});
SelectItem.displayName = SelectPrimitive.Item.displayName;

